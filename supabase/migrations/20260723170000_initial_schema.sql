create policy "school members can view school"
on public.schools for select to authenticated
using (id = public.current_school_id());

create policy "users can view own profile"
on public.profiles for select to authenticated
using (id = auth.uid() or public.is_school_staff(school_id));

create policy "admins can manage profiles"
on public.profiles for all to authenticated
using (public.is_school_admin(school_id))
with check (public.is_school_admin(school_id));

create policy "members can view groups"
on public.training_groups for select to authenticated
using (school_id = public.current_school_id());

create policy "staff can manage groups"
on public.training_groups for all to authenticated
using (public.is_school_staff(school_id))
with check (public.is_school_staff(school_id));

create policy "staff and guardians can view students"
on public.students for select to authenticated
using (public.is_school_staff(school_id) or guardian_user_id = auth.uid());

create policy "staff can manage students"
on public.students for all to authenticated
using (public.is_school_staff(school_id))
with check (public.is_school_staff(school_id));

create policy "staff and guardians can view fees"
on public.fee_periods for select to authenticated
using (
  public.is_school_staff(school_id)
  or exists (
    select 1 from public.students
    where students.id = fee_periods.student_id
      and students.guardian_user_id = auth.uid()
  )
);

create policy "staff can manage fees"
on public.fee_periods for all to authenticated
using (public.is_school_staff(school_id))
with check (public.is_school_staff(school_id));

