create extension if not exists pgcrypto;

create table public.schools (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  slug text not null unique,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  school_id uuid not null references public.schools(id) on delete cascade,
  full_name text not null,
  role text not null check (role in ('admin', 'staff', 'parent')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.training_groups (
  id uuid primary key default gen_random_uuid(),
  school_id uuid not null references public.schools(id) on delete cascade,
  name text not null,
  sort_order integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (school_id, name)
);

create table public.students (
  id bigint generated by default as identity primary key,
  school_id uuid not null references public.schools(id) on delete cascade,
  group_id uuid references public.training_groups(id) on delete set null,
  guardian_user_id uuid references auth.users(id) on delete set null,
  full_name text not null,
  birth_date date,
  position text check (position is null or position in ('Kaleci', 'Defans', 'Orta saha', 'Forvet')),
  guardian_name text,
  phone text,
  email text,
  address text,
  notes text,
  enrollment_date date not null default current_date,
  fee_tracking_start_date date,
  attendance_rate numeric(5,2) not null default 0 check (attendance_rate between 0 and 100),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.fee_periods (
  id bigint generated by default as identity primary key,
  school_id uuid not null references public.schools(id) on delete cascade,
  student_id bigint not null references public.students(id) on delete cascade,
  fee_month date not null check (fee_month = date_trunc('month', fee_month)::date),
  status text not null default 'none' check (status in ('none', 'late', 'paid', 'exempt', 'unknown')),
  amount numeric(12,2),
  due_date date,
  paid_at timestamptz,
  payment_method text check (payment_method is null or payment_method in ('cash', 'transfer', 'card')),
  note text,
  source text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (student_id, fee_month)
);

create table public.trainings (
  id bigint generated by default as identity primary key,
  school_id uuid not null references public.schools(id) on delete cascade,
  group_id uuid not null references public.training_groups(id) on delete restrict,
  training_date date not null,
  start_time time not null,
  duration_minutes integer not null default 90 check (duration_minutes > 0),
  title text not null,
  coach text not null,
  field text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.attendance_sessions (
  id bigint generated by default as identity primary key,
  school_id uuid not null references public.schools(id) on delete cascade,
  training_id bigint not null unique references public.trainings(id) on delete cascade,
  taken_by uuid references auth.users(id) on delete set null,
  taken_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.attendance_records (
  id bigint generated by default as identity primary key,
  session_id bigint not null references public.attendance_sessions(id) on delete cascade,
  student_id bigint not null references public.students(id) on delete cascade,
  present boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (session_id, student_id)
);

create table public.accounting_entries (
  id bigint generated by default as identity primary key,
  school_id uuid not null references public.schools(id) on delete cascade,
  student_id bigint references public.students(id) on delete set null,
  fee_period_id bigint references public.fee_periods(id) on delete set null,
  occurred_on date not null default current_date,
  title text not null,
  kind text not null check (kind in ('income', 'expense')),
  amount numeric(12,2) not null check (amount > 0),
  payment_method text not null default 'cash' check (payment_method in ('cash', 'transfer', 'card')),
  source text,
  reference text,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index accounting_entries_school_reference_key
  on public.accounting_entries (school_id, reference)
  where reference is not null;

create table public.notifications (
  id bigint generated by default as identity primary key,
  school_id uuid not null references public.schools(id) on delete cascade,
  audience text not null,
  title text not null,
  body text not null,
  status text not null default 'draft' check (status in ('draft', 'queued', 'sent', 'failed')),
  sent_by uuid references auth.users(id) on delete set null,
  sent_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.push_subscriptions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  endpoint text not null unique,
  p256dh text not null,
  auth_secret text not null,
  user_agent text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

