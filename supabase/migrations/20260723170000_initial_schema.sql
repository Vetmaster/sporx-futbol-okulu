create or replace function public.current_school_id()
returns uuid
language sql
stable
security definer
set search_path = public
as $$
  select school_id from public.profiles where id = auth.uid()
$$;

create or replace function public.current_user_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select role from public.profiles where id = auth.uid()
$$;

create or replace function public.is_school_staff(target_school_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.current_school_id() = target_school_id
    and public.current_user_role() in ('admin', 'staff')
$$;

create or replace function public.is_school_admin(target_school_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.current_school_id() = target_school_id
    and public.current_user_role() = 'admin'
$$;

revoke all on function public.current_school_id() from public;
revoke all on function public.current_user_role() from public;
revoke all on function public.is_school_staff(uuid) from public;
revoke all on function public.is_school_admin(uuid) from public;
grant execute on function public.current_school_id() to authenticated;
grant execute on function public.current_user_role() to authenticated;
grant execute on function public.is_school_staff(uuid) to authenticated;
grant execute on function public.is_school_admin(uuid) to authenticated;

